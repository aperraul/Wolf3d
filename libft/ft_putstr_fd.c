#include "libft.h"

void	ft_putstr_fd(char const *s, int fd)
{
	int	cpt;

	cpt = 0;
	while (s[cpt] != '\0')
	{
		ft_putchar_fd(s[cpt], fd);
		cpt++;
	}
}
