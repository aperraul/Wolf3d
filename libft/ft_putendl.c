#include "libft.h"

void	ft_putendl(char const *s)
{
	int	cpt;

	cpt = 0;
	while (s[cpt] != '\0')
	{
		ft_putchar(s[cpt]);
		cpt++;
	}
	ft_putchar('\n');
}
